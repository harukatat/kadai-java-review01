
public class CastSample {

    public static void main(String[] args) {
        // int型変数aとbの宣言及び初期化

        int a = 3;
        int b = 2;

        //
        System.out.println (a / b);
        //
        System.out.println ((double) a / b);
        //
        System.out.println ((double) (a / b));
    }

}
